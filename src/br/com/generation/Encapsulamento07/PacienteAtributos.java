package br.com.generation.Encapsulamento07;

public class PacienteAtributos {
	
	
	//Atributos do Cliente
	private String nome;
	private String doença;
	private int idade;
	private String tratRecom;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDoença() {
		return doença;
	}
	public void setDoença(String doença) {
		this.doença = doença;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getTratRecom() {
		return tratRecom;
	}
	public void setTratRecom(String tratRecom) {
		this.tratRecom = tratRecom;
	}
	
}
